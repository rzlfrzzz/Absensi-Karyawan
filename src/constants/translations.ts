export const getJabatanLabel = (lang: LanguageCode, id: string): string => {
  const jabatanMap: Record<string, string> = {
    HRD: 'j_hrd',
    Supervisor: 'j_spv',
    Admin: 'j_adm',
    Penjahit: 'j_tailor',
    Helper: 'j_helper',
    Picker: 'j_picker',
    Packing: 'j_packing',
  };
  const key = jabatanMap[id];
  if (!key) return id;

  const trans = translations[lang];
  return (trans as any)[key] || id;
};

